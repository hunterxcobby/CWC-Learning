

/* Addition*/
int add(int num1, int num2)
{
    int result;
    result = num1 + num2;

    return (result);
}

/* Multiplication*/
int mul(int num1, int num2)
{
    int result;
    result = num1 * num2;

    return(result);
}

int div(int num1, int num2)
{
    int result;

    result = num1 / num2;

    return(result);
}

int sub(int num1, int num2)
{
    int result;

    result = num1 - num2;

    return (result);
}